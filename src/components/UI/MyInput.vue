<script>
export default {
  name: 'MyInput',
  props: {
    modelValue: [String, Number]
  },
  methods: {
    updateInput(e) {
       this.$emit('update:modelValue', e.target.value)
    }
  }
}
</script>

<template>
  <input 
    :value="modelValue" 
    @input="updateInput" 
    class="input" 
    type="text"
  >
</template>

<style scoped>
.input {
  width:80%;
  border: 1px solid teal;
  padding: 10px 15px;
  margin-top: 15px;
}

</style>